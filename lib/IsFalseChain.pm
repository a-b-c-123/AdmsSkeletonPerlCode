sub new {
    my ($class, $function_ref) = @_;
    my $self = bless {}, $class;
    $self->{my_function} = $function_ref;
    return $self;
}

#-------------------------------------------------------------------------------
# Function : check
#-------------------------------------------------------------------------------
sub check {
    my ($self, @args) = @_;
    # Call the function stored in $self->{my_function} with arguments @args
    return not $self->{my_function}->(@args);
    if ($self->{my_function}->(@args)){
        return 0;
    }
    return 1;
}

